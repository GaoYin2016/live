package tv.yewai.live.douyu.vo;

public class ContentServerVo {
    private String id;
    private String nr;
    private String ml;
    private String ip;
    private String port;

    public String getId() {
        return this.id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNr() {
        return this.nr;
    }

    public void setNr(String nr) {
        this.nr = nr;
    }

    public String getMl() {
        return this.ml;
    }

    public void setMl(String ml) {
        this.ml = ml;
    }

    public String getIp() {
        return this.ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getPort() {
        return this.port;
    }

    public void setPort(String port) {
        this.port = port;
    }
}

